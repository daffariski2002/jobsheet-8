public class PerulanganBersarang {
    public static void main(String[] args) {
   
        // membuat variable
        int x, y;
        
        //melakukan perulangan sebanyak x dan y kali
        for (x =0; x <= 5; x++) {
            for( y =0; y <= 3; y++) {
                System.out.format("perulangan  [x=%d, y=%d] %n", x, y);
            }
        }
}
}
